
art_course = {'Economics','Hindi','History','Pol. Sc.'};
cm_course = {'Business St.','Economics','Accountancy', 'Maths'};

print(art_course.intersection(cm_course)); # intersection method returns common element among of them
print(art_course.difference(cm_course)); # this method returns distinct element from art_course
print(art_course.union(cm_course)); # this method returns all the unique elements from both sets


